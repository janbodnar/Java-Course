package com.zetcode;

public class IntegerNotationsEx {

    public static void main(String[] args) {
        
        int n1 = 31;
        int n2 = 0x31;
        int n3 = 031;
        int n4 = 0b1001;
        
        System.out.println(n1);
        System.out.println(n2);
        System.out.println(n3);
        System.out.println(n4);        
    }
}
