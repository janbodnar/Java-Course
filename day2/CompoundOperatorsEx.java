package com.zetcode;

public class CompoundOperatorsEx {

    public static void main(String[] args) {
        
        int a = 1;
        a = a + 1;
 
        System.out.println(a); 
        
        a += 5;
        System.out.println(a);    
        
        a *= 3;
        System.out.println(a);    
    }
}
